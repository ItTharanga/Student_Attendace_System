    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        try {
            int row = department_table.getSelectedRow();
            if (id_text.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Please select Department", "information box" 
                        + "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else if (isDepartmentExxist(name_text.getText(), id_text.getText())) {
            JOptionPane.showMessageDialog(null, "This department is already exist.", "information box" 
                        + "Warning", JOptionPane.INFORMATION_MESSAGE);
            } else {
            
                String sql = "update department set department_name = '"+name_text.getText()+"' where "
                        + " department_id = "+id_text.getText()+"";
                
                DB_Connection con = new DB_Connection();
                con.register(sql);
                department_table.setValueAt(name_text.getText(), row, 1);       
                department_table.clearSelection();
                clearTextFields();
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jToggleButton2ActionPerformed

    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed

        try {
            int row = department_table.getSelectedRow();
            if(row==-1){
            JOptionPane.showMessageDialog( null,"Please select department table", "information box" + "Warning", JOptionPane.INFORMATION_MESSAGE);
            }else{
                
                String depId = (department_table.getModel().getValueAt(row, 0).toString());
                String depName = (department_table.getModel().getValueAt(row, 1).toString());
                
                id_text.setText(depId);
                name_text.setText(depName);
            }
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jToggleButton3ActionPerformed

