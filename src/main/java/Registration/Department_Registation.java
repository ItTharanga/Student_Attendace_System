    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        try {
            clearTextFields();
            DefaultTableModel model = (DefaultTableModel)department_table.getModel();
            model.setRowCount(0);
            model.setRowCount(0);
            String sql = "select * from department";
            if (!search_combo_text.getText().equals("")) {
                sql = sql + " where department_name like '"+search_combo_text.getText()+"%'";
            }
                DB_Connection con = new DB_Connection();
                ResultSet r = con.search(sql);
                search_combo_text.setText("");
                while (r.next()) {
            
                model.addRow(new Object[]{r.getString("department_id"), r.getString("department_name")});
       
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

